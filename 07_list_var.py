# Q5 리스트의 + 와 extend()
# 두 방식의 차이는?
# + 는 새로운 리스트를 생성하는것임 기존 a 와 신규 a 주소가 바뀜
a = [1, 2, 3]
a = a + [4, 5]

# extend() 는 기존 리스트에 추가하므로 주소가 같음
