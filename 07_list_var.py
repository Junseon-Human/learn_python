import copy

# 리스트의 변수 활용 주의
a = 4
b = a
a = 3
# 위의 변수 a, b 는 각각 다름

a = [1, 2, 3]
b = a
a.append(4)
# 리스트의 경우는 주소를 복사하기때문에 a를 수정해도 b도 같이 바뀜

# 리스트의 값만을 복사하고싶을때
a = [1, 2, 3]

b = a[:]
c = list(a)
d = a.copy()

# 리스트 안에 리스트 가 있을때는 그 안의 리스트도 주소값을 가지고있기때문에
# 위의 방법으로 복사할수없음

# 리스트 안의 모든값만을 복사하려면 copy 를 import 하고 deepcopy() 를 사용함
a = [1, 2, 3, [1, 2]]
b = copy.deepcopy(a)
a[3][0] = 4

